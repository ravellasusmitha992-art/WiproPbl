package Hourplanm1;

public class SumEvenOdd {

	public static void main(String[] args) {
		int a = 10;
        int b = 5;

        int sum = a + b;

        if (sum % 2 == 0) {
            System.out.println("Sum is Even");
        } else {
            System.out.println("Sum is Odd");
        }
    }
}